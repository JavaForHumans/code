package helloWorld.simpleExample;

/**
 * The main class holds the main method which is called
 * by the computer to run your program.
 *
 * Your main class must be public so that the computer can see it.
 *
 * Don't worry, we will go over all of this and more in the coming chapters.
 */
public class Main {

    /**
     * The main method is where your entire program starts from.
     * The main method is public and static,
     * so the computer does not need an instance of your ConsoleGameMain class to run it.
     *
     * Your code is executed in order starting from here.
     *
     * @param args the arguments the computer provides to this method when it calls it.
     */
    public static void main(String[] args){
        System.out.println("Hello, World!");
    }
}
